from backend import db

class PlayerYearlyStats(db.Model):
    __tablename__ = 'PlayerYearlyStats'
    player_id = db.Column(db.String(50), primary_key=True)
    season = db.Column(db.Integer, primary_key=True)
    season_type = db.Column(db.String(20), primary_key=True)
    team_id = db.Column(db.String(10), db.ForeignKey('DimTeams.team_id'))

    shotgun = db.Column(db.Integer)
    no_huddle = db.Column(db.Integer)
    qb_dropback = db.Column(db.Integer)
    qb_scramble = db.Column(db.Integer)
    pass_attempts = db.Column(db.Integer)
    complete_pass = db.Column(db.Integer)
    incomplete_pass = db.Column(db.Integer)
    passing_yards = db.Column(db.Float)
    receiving_yards = db.Column(db.Float)
    yards_after_catch = db.Column(db.Float)
    rush_attempts = db.Column(db.Integer)
    rushing_yards = db.Column(db.Float)
    tackled_for_loss = db.Column(db.Integer)
    first_down_pass = db.Column(db.Integer)
    first_down_rush = db.Column(db.Integer)
    third_down_converted = db.Column(db.Integer)
    third_down_failed = db.Column(db.Integer)
    fourth_down_converted = db.Column(db.Integer)
    fourth_down_failed = db.Column(db.Integer)
    rush_touchdown = db.Column(db.Integer)
    pass_touchdown = db.Column(db.Integer)
    receiving_touchdown = db.Column(db.Integer)
    receptions = db.Column(db.Integer)
    targets = db.Column(db.Integer)
    passing_air_yards = db.Column(db.Float)
    receiving_air_yards = db.Column(db.Float)
    fantasy_points_ppr = db.Column(db.Float)
    fantasy_points_standard = db.Column(db.Float)
    total_tds = db.Column(db.Integer)
    touches = db.Column(db.Integer)
    total_yards = db.Column(db.Float)
    offense_snaps = db.Column(db.Integer)
    team_offense_snaps = db.Column(db.Integer)
    offense_pct = db.Column(db.Float)
    solo_tackle = db.Column(db.Integer)
    assist_tackle = db.Column(db.Integer)
    tackle_with_assist = db.Column(db.Integer)
    sack = db.Column(db.Float)
    qb_hit = db.Column(db.Integer)
    def_touchdown = db.Column(db.Integer)
    defensive_two_point_attempt = db.Column(db.Integer)
    defensive_two_point_conv = db.Column(db.Integer)
    defensive_extra_point_attempt = db.Column(db.Integer)
    defensive_extra_point_conv = db.Column(db.Integer)
    defense_snaps = db.Column(db.Integer)
    team_defense_snaps = db.Column(db.Integer)
    defense_pct = db.Column(db.Float)
    age = db.Column(db.Integer)
    safety = db.Column(db.Integer)
    interception = db.Column(db.Integer)
    fumble = db.Column(db.Integer)
    fumble_lost = db.Column(db.Integer)
    fumble_forced = db.Column(db.Integer)
    fumble_not_forced = db.Column(db.Integer)
    fumble_out_of_bounds = db.Column(db.Integer)